from __future__ import annotations

from pathlib import Path

from .models import ClaimInput, DecisionType
from .orchestrator import ClaimOrchestrator
from .policy_loader import load_test_cases


ROOT = Path(__file__).resolve().parents[2]
REPORT_PATH = ROOT / "docs" / "eval_report.md"


async def run_case(test_case: dict) -> dict:
    orchestrator = ClaimOrchestrator()
    response = await orchestrator.process(ClaimInput(**test_case["input"]))
    expected = test_case["expected"]
    expected_decision = expected.get("decision")

    if expected_decision is None:
        passed = response.decision == DecisionType.BLOCKED
    else:
        passed = response.decision.value == expected_decision
        if expected.get("approved_amount") is not None:
            passed = passed and response.approved_amount == float(expected["approved_amount"])
    passed = passed and bool(response.reason) and bool(response.evidence) and bool(response.trace)

    return {
        "case_id": test_case["case_id"],
        "case_name": test_case["case_name"],
        "passed": passed,
        "expected_decision": expected_decision or "BLOCKED",
        "actual_decision": response.decision.value,
        "approved_amount": response.approved_amount,
        "confidence_score": response.confidence_score,
        "reason": response.reason,
        "evidence": response.evidence,
        "trace": response.trace,
        "response": response,
    }


async def run_all_cases(write_report: bool = True) -> list[dict]:
    results = [await run_case(test_case) for test_case in load_test_cases()]
    if write_report:
        write_eval_report(results)
    return results


def write_eval_report(results: list[dict]) -> None:
    lines = [
        "# Eval Report",
        "",
        "Generated by `backend.app.eval_runner` from `test_cases.json`.",
        "",
        "| Case | Expected | Actual | Approved | Confidence | Result |",
        "| --- | --- | --- | ---: | ---: | --- |",
    ]
    for result in results:
        lines.append(
            f"| {result['case_id']} | {result['expected_decision']} | {result['actual_decision']} | "
            f"Rs {result['approved_amount']:.0f} | {result['confidence_score']:.2f} | "
            f"{'PASS' if result['passed'] else 'FAIL'} |"
        )

    for result in results:
        lines.extend([
            "",
            f"## {result['case_id']} - {result['case_name']}",
            "",
            f"**Result:** {'PASS' if result['passed'] else 'FAIL'}",
            "",
            f"**Decision:** {result['actual_decision']}",
            "",
            f"**Approved Amount:** Rs {result['approved_amount']:.0f}",
            "",
            f"**Confidence:** {result['confidence_score']:.2f}",
            "",
            f"**Reason:** {result['reason']}",
            "",
            "**Evidence:**",
        ])
        for item in result["evidence"][:12]:
            lines.append(f"- {item}")
        lines.extend(["", "**Trace:**"])
        for trace in result["trace"]:
            lines.append(f"- `{trace.agent}` {trace.status.value}: {trace.summary}")
            for check in trace.checks:
                rule = f" `{check.rule_id}`" if check.rule_id else ""
                lines.append(f"  -{rule} {check.result}: {check.details}")

    REPORT_PATH.parent.mkdir(parents=True, exist_ok=True)
    REPORT_PATH.write_text("\n".join(lines) + "\n", encoding="utf-8")
